const users = [
    {name: 'Manolo el del bombo',
        favoritesSounds: {
            waves: {format: 'mp3', volume: 50},
            rain: {format: 'ogg', volume: 60},
            firecamp: {format: 'mp3', volume: 80},
        }
    },
    {name: 'Mortadelo',
        favoritesSounds: {
            waves: {format: 'mp3', volume: 30},
            shower: {format: 'ogg', volume: 55},
            train: {format: 'mp3', volume: 60},
        }
    },
    {name: 'Super Lopez',
        favoritesSounds: {
            shower: {format: 'mp3', volume: 50},
            train: {format: 'ogg', volume: 60},
            firecamp: {format: 'mp3', volume: 80},
        }
    },
    {name: 'El culebra',
        favoritesSounds: {
            waves: {format: 'mp3', volume: 67},
            wind: {format: 'ogg', volume: 35},
            firecamp: {format: 'mp3', volume: 60},
        }
    },
]
//for of para iterar sobre el array users y for in para iterar sobre los sonidos favoritos de cada user:

let totalVol = 0;

for (const user of users) {
    for (const sound in user.favoritesSounds) {
        totalVol += user.favoritesSounds[sound].volume;
    }
}

const mediaVol = totalVol / (users.length * Object.keys(users[0].favoritesSounds).length);
console.log("La media del volumen de los sonidos favoritos es:", mediaVol);
